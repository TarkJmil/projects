#الاستدعاء الذاتي هو مفهوم في البرمجة الذي يتيح لبرنامج أن يكرر نفسه بشكل دوري. 
# يحدث الاستدعاء الذاتي عندما يطلب البرنامج استدعاء نفسه من جديد، مما يؤدي إلى تكرار نفس العملية بشكل دوري.

#مثال بسيط:


def qq(y):
    if y == 0:
        return 0
    else:
        return y + qq(y-1)

print(qq(6))  # Output: 15