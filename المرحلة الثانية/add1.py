#لغة بايثون

#بايثون (Python) هي لغة برمجة عالية المستوى، متعددة الاستخدامات، سهلة التعلم وسهلة الاستخدام. تم تطويرها في الثمانينيات من القرن الماضي من قبل غودفري سينز وتم إصدارها لأول مرة في عام 1991.

